function findNextNumber(X) {
  // X를 문자열로 변환하여 각 자리 숫자를 배열에 저장
  const XArray = X.toString().split("");

  // X를 1씩 증가시키면서 구성이 같은 숫자를 찾음
  while (true) {
    X++;

    // 현재 X를 문자열로 변환하여 각 자리 숫자를 배열에 저장
    const currentArray = X.toString().split("");

    // X와 현재 배열이 같으면 X를 출력하고 종료
    if (XArray.join("") === currentArray.join("")) {
      console.log(X);
      return;
    }

    // X가 999999보다 크면 구성이 같은 숫자가 없는 경우이므로 0을 출력하고 종료
    if (X > 999999) {
      console.log(0);
      return;
    }
  }
}

